l1= [5,85,2,65,41,21,17,9]
print(l1)

l1.sort()          #this will sort the list in ascending order
print(l1)

l1.reverse()       #this will rverse the list
print(l1)

l1.append(91)      # append is used to add data at the end of list
print(l1)

l1.insert(2,6)     #this will insert 6 at index 2
print(l1)

l1.pop(2)          #this will remove data from index 2
print(l1)


l1.remove(21)      #this will remove data 21 from list
print(l1)